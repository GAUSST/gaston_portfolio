import { initReveal } from "./ui.js";

const LANG_KEY = "gd_lang";
export let lang =
  localStorage.getItem(LANG_KEY) ||
  (navigator.language?.startsWith("fr") ? "fr" : "en");

function getByPath(obj, path) {
  return path
    .split(".")
    .reduce((o, k) => (o && k in o ? o[k] : undefined), obj);
}

// Render functions for static sections
function renderExperience() {
  const box = document.querySelector("#experience .timeline");
  if (!box) return;
  box.innerHTML = (I18N[lang].experience || [])
    .map(
      (item) => `
      <div class="tl-item">
        <h3>${item.title}</h3>
        <p class="small">${item.period}</p>
        <p>${item.desc || ""}</p>
      </div>
    `
    )
    .join("");
}

function renderServices() {
  const grid = document.getElementById("servicesGrid");
  if (!grid) return;
  grid.innerHTML = (I18N[lang].services?.items || [])
    .map(
      (item) => `
      <article class="card reveal service-card">
        <div class="card-icon" aria-hidden="true"><i class="${item.icon}"></i></div>
        <h3>${item.title}</h3>
        <p>${item.text}</p>
      </article>
    `
    )
    .join("");
}

function renderPackages() {
  const grid = document.getElementById("packagesGrid");
  if (!grid) return;
  grid.innerHTML = (I18N[lang].packages?.items || [])
    .map(
      (item) => `
      <article class="card reveal package-card">
        <div class="package-head">
          <div class="card-icon" aria-hidden="true"><i class="${item.icon}"></i></div>
          <div>
            <h3>${item.title}</h3>
            <p class="package-price">${item.price}</p>
          </div>
        </div>
        <p>${item.text}</p>
        <ul class="feature-list">
          ${(item.includes || []).map((entry) => `<li>${entry}</li>`).join("")}
        </ul>
        <a class="btn secondary package-cta" href="#contact">${I18N[lang].packages.cta}</a>
      </article>
    `
    )
    .join("");
}

function renderStaticSections() {
  renderServices();
  renderPackages();
  renderExperience();
  const obs = initReveal();
  document.querySelectorAll(".reveal").forEach((el) => obs.observe(el));
}

export function applyI18n(refreshListCallback) {
  document.documentElement.lang = lang;

  // Meta
  document.title = I18N[lang].meta.title;
  const metaDesc = document.querySelector('meta[name="description"]');
  if (metaDesc) metaDesc.setAttribute("content", I18N[lang].meta.desc);

  // Data-bound text nodes
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    const val = getByPath(I18N[lang], key);
    if (val != null) {
      if (el.hasAttribute("data-i18n-html")) el.innerHTML = val;
      else el.textContent = val;
    }
  });

  // Attribute translations
  document.querySelectorAll("[data-i18n-attr]").forEach((el) => {
    const pairs = el.getAttribute("data-i18n-attr").split("|");
    pairs.forEach((pair) => {
      const [attr, key] = pair.split(":");
      const val = getByPath(I18N[lang], key);
      if (attr && val != null) el.setAttribute(attr.trim(), val);
    });
  });

  // Theme toggle label
  const themeBtn = document.getElementById("themeToggle");
  if (themeBtn) themeBtn.setAttribute("aria-label", I18N[lang].toggles.theme);

  // Lang toggle label text
  const langBtn = document.getElementById("langToggle");
  if (langBtn) langBtn.textContent = I18N[lang].toggles.langBtn;

  // Menu toggle aria-label
  const menuBtn = document.getElementById("menuToggle");
  if (menuBtn) {
    const expanded = menuBtn.getAttribute("aria-expanded") === "true";
    menuBtn.setAttribute(
      "aria-label",
      expanded
        ? I18N[lang]?.toggles?.menuClose || "Close menu"
        : I18N[lang]?.toggles?.menuOpen || "Open menu"
    );
  }

  // Search + empty state + view toggle
  const searchEl = document.getElementById("projectSearch");
  if (searchEl) searchEl.placeholder = I18N[lang].projects.searchPH;
  const emptyEl = document.getElementById("emptyState");
  if (emptyEl) emptyEl.textContent = I18N[lang].projects.empty;

  // CV Link
  const cv_btn = document.querySelector(".actions .btn.cv") || document.querySelector(".nav-utilities .btn.cv");
  if (cv_btn) {
      cv_btn.href = lang === "fr" ? "assets/Dev_CV_French.pdf" : "assets/Dev_CV_English.pdf";
  }

  // Update Toggle Text (View more/less)
  const toggleBtn = document.getElementById("toggleProjects");
  if (toggleBtn) {
      // We need to know the state of showAll to set correct text. 
      // This is a bit coupled. For now, we'll just default to "View more" or let the callback handle it.
      // Or we can expose a helper to update just the text.
      // Actually, refreshListCallback handles re-rendering projects which updates the button visibility, 
      // but maybe not the text if it depends on state in projects.js.
      // Let's leave it to the callback or a separate update.
  }

  renderStaticSections(); 
  
  if (refreshListCallback) refreshListCallback();
}

export function toggleLanguage(refreshListCallback, relocateCallback) {
  lang = lang === "en" ? "fr" : "en";
  localStorage.setItem(LANG_KEY, lang);
  applyI18n(refreshListCallback);
  if (relocateCallback) relocateCallback();
}

export function initLanguage(refreshListCallback, relocateCallback) {
  const actions = document.querySelector("header.nav .actions");
  if (!actions) return;
  let btn = document.getElementById("langToggle");
  if (!btn) {
    btn = document.createElement("button");
    btn.id = "langToggle";
    btn.className = "btn secondary icon-btn";
    btn.type = "button";
    btn.title = "Toggle language";
    btn.setAttribute("aria-label", "Toggle language");
    btn.textContent = I18N[lang].toggles.langBtn;
    actions.insertBefore(btn, actions.firstChild);
  }
  btn.onclick = () => toggleLanguage(refreshListCallback, relocateCallback);
  
  // Initial apply
  applyI18n(refreshListCallback);
}
